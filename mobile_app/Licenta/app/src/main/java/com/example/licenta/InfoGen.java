package com.example.licenta;

public class InfoGen {
    private String greutate;
    private String inaltime;
    private String grupa_sange;
    private String rh;
    private String puls;
    private String tensiune;

    public InfoGen() {
    }

    private String data_naster;

    public String getGreutate() {
        return greutate;
    }

    public void setGreutate(String greutate) {
        this.greutate = greutate;
    }

    public String getInaltime() {
        return inaltime;
    }

    public void setInaltime(String inaltime) {
        this.inaltime = inaltime;
    }

    public String getGrupa_sange() {
        return grupa_sange;
    }

    public void setGrupa_sange(String grupa_sange) {
        this.grupa_sange = grupa_sange;
    }

    public String getRh() {
        return rh;
    }

    public void setRh(String rh) {
        this.rh = rh;
    }

    public String getPuls() {
        return puls;
    }

    public void setPuls(String puls) {
        this.puls = puls;
    }

    public String getTensiune() {
        return tensiune;
    }

    public void setTensiune(String tensiune) {
        this.tensiune = tensiune;
    }

    public String getData_naster() {
        return data_naster;
    }

    public void setData_naster(String data_naster) {
        this.data_naster = data_naster;
    }
}
